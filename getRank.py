
def get_rank(user_review, list_review):

    """

    Returns where a number ranks in a distribution(Percentile)

    Args:
        user_review: A value, string or int
        list_review: An iterable, preferably a list

    Returns: A float

    """

    count = 0
    for i in sorted(list(map(int, list_review))):
        if i >= int(user_review):
            break
        count += 1
    return 100 - (count*100)/len(list_review)


